function Map(width, height, image) {
	this.width = width; // Must be a multiple of 32 (Tile width)
	this.height = height; // Must be a multiple of 32 (Tile height)
	this.image = new Image();
	this.image.src = image;
	this.tileSize = 32;
	this.tilesX = this.width / this.tileSize;
	this.tilesY = this.height / this.tileSize;
	this.dynamicMap = this.testDynamicMap();
	this.foregroundMap = this.testForegroundMap();

	this.tiles = new Array();
	this.tiles.push(new Image()); // Blank
	this.tiles.push(new Image()); // Blank
	this.tiles.push(new Image()); // Blank
	this.tiles.push(new Image());
	this.tiles[this.tiles.length - 1].src = 'img/wall.png';

	this.items = new Array();
}

Map.prototype.drawBackground = function(context) {
	 context.drawImage(this.image, 0, 0);
}

Map.prototype.drawForeground = function(context, spriteSheet) {	
	 for(var y = 0; y < this.dynamicMap.length ; y++) {
	 	for(var x = 0; x < this.dynamicMap[y].length; x++) {
	 		switch(this.foregroundMap[y][x]) {
	 			case 1:
	 				context.drawImage(spriteSheet, 0, 160, 32, 32, x * 32, y * 32, 32, 32);
	 			break;
	 			case 2:
	 				context.drawImage(spriteSheet, 32, 160, 32, 32, x * 32, y * 32, 32, 32);
	 			break;	
	 			case 3:
	 				context.drawImage(spriteSheet, 64, 160, 32, 32, x * 32, y * 32, 32, 32);
	 			break;	
	 			case 4:
	 				context.drawImage(spriteSheet, 96, 160, 32, 32, x * 32, y * 32, 32, 32);
	 			break;	
	 		}
	 	}
	}
}

